/**
 * Get add operation of two numbers.
 * 
 * @param {number} numA - The first numeric operand
 * @param {number} numB - The second numeric operand
 * @returns {number} Sum of numA and numB
 */
function add(numA, numB) {
  return numA + numB;
}

