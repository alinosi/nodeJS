// reverse
// membaik indeks dari abc menjadi cba
const myArray = ['Android', 'Data Science', 'Web'];
myArray.reverse();
console.log(myArray); // Output: [ 'Web', 'Data Science', 'Android' ]


// Sort
// mengurutkan array berdasarkan abjad(tergantung pilihan) descending/ascending
const mysort = ['Web', 'Android', 'Data Science',21,1];
mysort.sort();
console.log(mysort); // Output: [ 'Android', 'Data Science', 'Web' ]

